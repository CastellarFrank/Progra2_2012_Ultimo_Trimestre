private void btnImagenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnImagenMouseClicked
    this.path="";
    JFileChooser file=new JFileChooser();
    file.setDialogTitle("Eliga una imagen");
    file.setFileSelectionMode(JFileChooser.FILES_ONLY);
    int val=file.showOpenDialog(this);
    if(val==JFileChooser.APPROVE_OPTION){
        String temp=file.getSelectedFile().getAbsolutePath();
        System.out.println(temp);
        colocarImagen(temp);
        this.path=temp;
    }else{
        JOptionPane.showMessageDialog(this, "No ha seleccionado ningún archivo","Información inválida",
                JOptionPane.ERROR_MESSAGE);
    }
}//GEN-LAST:event_btnImagenMouseClicked

