    private void colocarImagen(String temp) {
        ImageIcon image=new ImageIcon(temp);
        ImageIcon image2=new ImageIcon(image.getImage().getScaledInstance(this.lblImagen.getWidth(), 
                this.lblImagen.getHeight(), Image.SCALE_DEFAULT));
        this.lblImagen.setIcon(image2);
    }
