private void btnRegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegistrarMouseClicked
    if(this.txtName.getText().isEmpty() || this.txtPassword.getText().isEmpty() ||
            this.txtApellido.getText().isEmpty() || this.path.isEmpty()){
        System.out.println("Nombre: "+this.txtName.getText().isEmpty()+" - "+
                "Password: "+this.txtPassword.getText().isEmpty()+" - "+
                "Apellido: "+this.txtApellido.getText().isEmpty()+" - "+
                "Path: "+this.path.isEmpty());
        JOptionPane.showMessageDialog(this, "El código o la contraseña están vacios",
                "Información necesaria",JOptionPane.WARNING_MESSAGE);
    }else{
        Usuarios temp=new Usuarios(this.txtName.getText(),this.txtPassword.getText(),
                this.txtApellido.getText(),this.path);
        this.informacion.add(temp);
        
        //this.informacion.add(new Usuarios(this.txtName.getText(),this.txtPassword.getText()));
        JOptionPane.showMessageDialog(this,"Información guardada con éxito","Estás registrado",
                JOptionPane.INFORMATION_MESSAGE);
        
        this.setVisible(false);
        
    }
}//GEN-LAST:event_btnRegistrarMouseClicked

